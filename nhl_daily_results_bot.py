def _ru_initial_surname_from_profile(url: str) -> str | None:
    """
    –û—Ç–∫—Ä—ã–≤–∞–µ–º –ø—Ä–æ—Ñ–∏–ª—å –∏–≥—Ä–æ–∫–∞ –Ω–∞ sports.ru –∏ –±–µ—Ä—ë–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ h1 ‚Üí "–ò–º—è –§–∞–º–∏–ª–∏—è".
    –í–æ–∑–≤—Ä–∞—â–∞–µ–º "–ò. –§–∞–º–∏–ª–∏—è".
    """
    try:
        r = S.get(url, timeout=25)
        if r.status_code != 200:
            return None
        soup = BeautifulSoup(r.text, "html.parser")
        h = soup.find(["h1", "h2"])
        if not h:
            return None
        full = " ".join(h.get_text(" ", strip=True).split())
        parts = [p for p in re.split(r"\s+", full) if p]
        if len(parts) >= 2:
            ini = parts[0][0] + "."
            last = parts[-1]
            return f"{ini} {last}"
    except Exception as e:
        log("[sports.ru] profile parse fail:", e)
    return None

def ru_initial_surname_by_en(first: str, last: str, display: str | None = None) -> str:
    """
    ¬´–ò. –†—É—Å—Å–∫–∞—è–§–∞–º–∏–ª–∏—è¬ª (–∏–ª–∏ ¬´I. Lastname¬ª, –µ—Å–ª–∏ –ø—Ä–æ—Ñ–∏–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω).
    –ï—Å–ª–∏ display —É–∂–µ —Ç–∏–ø–∞ "C. McDavid" ‚Äî –º–æ–∂–Ω–æ –≤–µ—Ä–Ω—É—Ç—å –µ–≥–æ —Å—Ä–∞–∑—É –∫–∞–∫ fallback.
    """
    # –µ—Å–ª–∏ –µ—Å—Ç—å –≥–æ—Ç–æ–≤–∞—è –∫—Ä–∞—Å–∏–≤–∞—è –ª–∞—Ç–∏–Ω–∏—Ü–∞
    if display:
        disp = display.replace("#", " ").strip()
        # —Ñ–∏–ª—å—Ç—Ä—É–µ–º –æ—á–µ–Ω—å —Å—Ç—Ä–∞–Ω–Ω—ã–µ —à—Ç—É–∫–∏
        if 2 <= len(disp) <= 40 and any(c.isalpha() for c in disp):
            # –ø–æ–ø—Ä–æ–±—É–µ–º –≤—Å—ë —Ä–∞–≤–Ω–æ –ø–æ–ª—É—á–∏—Ç—å —Ä—É—Å—Å–∫—É—é —Ñ–∞–º–∏–ª–∏—é ‚Äî –Ω–æ –µ—Å–ª–∏ –Ω–µ –≤—ã–π–¥–µ—Ç, –≤–µ—Ä–Ω—ë–º display
            pass

    first = (first or "").strip()
    last  = (last  or "").strip()
    key = f"{first} {last}".strip()
    if key in _ru_name_cache:
        return _ru_name_cache[key]

    # 1) –ø–æ–∏—Å–∫ sports.ru
    if key:
        try:
            q = quote_plus(key)
            sr = S.get(SPORTS_RU_SEARCH + q, timeout=25)
            if sr.status_code == 200:
                soup = BeautifulSoup(sr.text, "html.parser")
                link = soup.select_one('a[href*="/hockey/person/"]') or soup.select_one('a[href*="/hockey/player/"]')
                if link and link.get("href"):
                    href = link["href"]
                    if href.startswith("/"):
                        href = "https://www.sports.ru" + href
                    ru = _ru_initial_surname_from_profile(href)
                    if ru:
                        _ru_name_cache[key] = ru
                        return ru
        except Exception as e:
            log("[sports.ru] search fail:", key, e)

    # 2) fallback: display (–ª–∞—Ç–∏–Ω–∏—Ü–∞) –∏–ª–∏ —Å–∫–ª–µ–∏—Ç—å I. Lastname
    if display:
        _ru_name_cache[key or display] = display
        return display
    lat = (first[:1] + ". " if first else "") + (last or key or "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ")
    _ru_name_cache[key or lat] = lat
    return lat

def resolve_player_ru_initial(pid: int, boxmap: dict, players_involved: list) -> str:
    """
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç ¬´–ò. –†—É—Å—Å–∫–∞—è–§–∞–º–∏–ª–∏—è¬ª –¥–ª—è –∏–≥—Ä–æ–∫–∞:
    boxscore ‚Üí playersInvolved ‚Üí landing ‚Üí –ª–∞—Ç–∏–Ω–∏—Ü–∞/ID.
    """
    # 1) boxmap
    if pid and pid in boxmap:
        f = boxmap[pid].get("firstName", "")
        l = boxmap[pid].get("lastName", "")
        disp = _display_cache.get(pid)  # –º–æ–∂–µ—Ç –±—ã—Ç—å "C. McDavid"
        if f or l or disp:
            return ru_initial_surname_by_en(f, l, disp)

    # 2) playersInvolved
    for p in (players_involved or []):
        if p.get("playerId") == pid:
            f, l, d = _extract_names_from_player_obj(p)
            if f or l or d:
                return ru_initial_surname_by_en(f, l, d)

    # 3) landing
    f, l = fetch_player_en_name(pid)
    if f or l:
        return ru_initial_surname_by_en(f, l)

    # 4) –∫—Ä–∞–π–Ω–∏–π —Å–ª—É—á–∞–π ‚Äî –æ—Ç–¥–∞—ë–º ID
    return f"#{pid}"

