def parse_time_to_sec_in_period(t: str) -> int:
    """ 'MM:SS' –∏–ª–∏ 'M:SS' ‚Üí —Å–µ–∫—É–Ω–¥—ã –≤ –ø–µ—Ä–∏–æ–¥–µ. """
    try:
        m, s = str(t).split(":")
        return int(m)*60 + int(s)
    except Exception:
        try:
            return int(t)*60
        except Exception:
            return 0

def period_to_index(period_type: str, number: int) -> int:
    """REG: 1..3; OT: 4; SO: 5 (—Å—Ç—Ä–æ–≥–æ –ø–æ—Å–ª–µ –∏–≥—Ä—ã)."""
    pt = (period_type or "").upper()
    if pt == "OT": return 4
    if pt == "SO": return 5
    return max(1, int(number or 1))

def abs_seconds(period_index: int, sec_in_period: int) -> int:
    """
    –ê–±—Å–æ–ª—é—Ç–Ω—ã–µ —Å–µ–∫—É–Ω–¥—ã —Å –Ω–∞—á–∞–ª–∞ –º–∞—Ç—á–∞, –µ—Å–ª–∏ 20-–º–∏–Ω—É—Ç–Ω—ã–µ –ø–µ—Ä–∏–æ–¥—ã:
    (period-1)*1200 + sec. –î–ª—è SO —Å—á–∏—Ç–∞–µ–º –±–∞–∑—É 65:00 (3900 —Å–µ–∫) + —à–∞–≥ 1 —Å–µ–∫.
    """
    if period_index == 5:  # SO
        return 65*60 + sec_in_period  # 65:00 + –ø—Å–µ–≤–¥–æ-—Å–µ–∫—É–Ω–¥—ã –ø–æ–ø—ã—Ç–æ–∫
    if period_index >= 4:  # OT
        return 60*60 + sec_in_period
    return (period_index - 1)*20*60 + sec_in_period

def fmt_mm_ss(total_seconds: int) -> str:
    mm = total_seconds // 60
    ss = total_seconds % 60
    return f"{mm}.{ss:02d}"

